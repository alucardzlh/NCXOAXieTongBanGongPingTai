package com.OA.Entity;

public class FirstNextStepDatas {
	private String FlowAction;
	private String FlowDescript;
	private String NextStepID;
	private String CheckCode;
	private String CheckName;
	private String ElementLabelName;
	public String getFlowAction() {
		return FlowAction;
	}
	public void setFlowAction(String flowAction) {
		FlowAction = flowAction;
	}
	public String getFlowDescript() {
		return FlowDescript;
	}
	public void setFlowDescript(String flowDescript) {
		FlowDescript = flowDescript;
	}
	public String getNextStepID() {
		return NextStepID;
	}
	public void setNextStepID(String nextStepID) {
		NextStepID = nextStepID;
	}
	public String getCheckCode() {
		return CheckCode;
	}
	public void setCheckCode(String checkCode) {
		CheckCode = checkCode;
	}
	public String getCheckName() {
		return CheckName;
	}
	public void setCheckName(String checkName) {
		CheckName = checkName;
	}
	public String getElementLabelName() {
		return ElementLabelName;
	}
	public void setElementLabelName(String elementLabelName) {
		ElementLabelName = elementLabelName;
	}
	
}
